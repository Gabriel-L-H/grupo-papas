function botaomais01() {
    const quant = document.getElementById("quantidades01")
    var numeroatual = parseInt(quant.innerHTML)
    var numeronovo = numeroatual + 1
    quant.innerHTML = numeronovo
}

function botaomenos01() {
    const quant = document.getElementById("quantidades01")
    var numeroatual = parseInt(quant.innerHTML)
    if (numeroatual >= 1) {
        var numeronovo = numeroatual - 1
        quant.innerHTML = numeronovo
    } else {
        quant.innerHTML = 0
    }
}

